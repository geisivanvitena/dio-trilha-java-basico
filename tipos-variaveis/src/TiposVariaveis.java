public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        System.out.println("Exemplo de tipos de variaveis");

        // Tipos inteiros
        byte b = 100;
        short s = 20000;
        int i = 500000;
        long l = 10000000000l;

        // Tipos decimais
        float f = 5.75f;
        double d = 19.99;

        // Caracter
        char c = 'M';

        // Boleano
        boolean ativo = true;

        // String 
        String nome = "Geisivan";

        
    }

   


}
