public class Operadores {
    
    public static void main(String[] args) {

        System.out.println("Operadores");

        // Operadores Aritméticos
        // +   Adição
        // -   Subtração
        // *   Multiplicação
        // /   Divisão
        // %   Resto da divisão

        // Operadores Relacionais
        // >   Maior que
        // <   Menor que
        // >=  Maior ou igual a
        // <=  Menor ou igual a
        // ==  Igual a
        // !=  Diferente de

        // Operadores Lógicos
        // &&  E lógico (AND)
        // ||  Ou lógico (OR)
        // !   Negação lógica (NOT)

        // Operadores de Atribuição
        // =   Atribuição simples
        // +=  Adição e atribuição
        // -=  Subtração e atribuição
        // *=  Multiplicação e atribuição
        // /=  Divisão e atribuição
        // %=  Resto e atribuição

        // Operadores Unários
        // ++  Incremento
        // --  Decremento
        // +   Sinal positivo
        // -   Sinal negativo
        // !   Negação lógica

    }

  

  

}
